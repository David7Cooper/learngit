package com.struts.cooper.bean;

public class User {
	private String name;
	private String password;
	/**
	 * 存取器
	 */
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	/**
	 * End
	 */
}
